package HomeTheater;

public class DVDPlayer {

  public void on() {
    System.out.println("dvd is on");
  }

  public void off() {
    System.out.println("dvd is off");
  }

  public void pause() {
    System.out.println("Pause the current playing DVD");
  }

  public void play() {
    System.out.println("play the dvd");
  }

  public void stop() {
    System.out.println("stop the dvd player");
  }
}
